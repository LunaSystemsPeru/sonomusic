    private void mostrar_tabla() {
        String query = "select a.id_almacen, a.nombre, a.direccion, a.ciudad, a.estado, e.ruc, e.razon "
                + "from almacen as a "
                + "inner join empresa as e on e.id_empresa = a.id_empresa "
                + "order by a.nombre asc";
        c_almacen.mostrar(t_tiendas, query);
    }

