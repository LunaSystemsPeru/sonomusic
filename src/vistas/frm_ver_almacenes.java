    private void btn_j_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_j_grabarActionPerformed
        cla_empresa o_empresa = (cla_empresa) cbx_j_empresa.getSelectedItem();
        c_almacen.setEmpresa(o_empresa.getId_empresa());
        c_almacen.setNombre(txt_j_nombre.getText());
        c_almacen.setDireccion(txt_j_direccion.getText());
        c_almacen.setCiudad(txt_j_ciudad.getText());
        c_almacen.setTicketera(txt_j_ticketera.getText());

        if (c_almacen.getId() == 0) {
            c_almacen.obtener_codigo();
            c_almacen.registrar();
        } else {
            c_almacen.modificar();
        }

        mostrar_tabla();
        jd_reg_tienda.dispose();
    }//GEN-LAST:event_btn_j_grabarActionPerformed

    private void t_tiendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_tiendasMouseClicked
        if (evt.getClickCount() == 2) {
            fila_seleccionada = t_tiendas.getSelectedRow();
            c_almacen.setId(Integer.parseInt(t_tiendas.getValueAt(fila_seleccionada, 0).toString()));
            btn_modificar.setEnabled(true);
            btn_doc_sunat.setEnabled(true);
        }
    }//GEN-LAST:event_t_tiendasMouseClicked

    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        jd_reg_tienda.setModal(true);
        jd_reg_tienda.setSize(621, 334);
        jd_reg_tienda.setLocationRelativeTo(null);

        btn_modificar.setEnabled(false);
        activar_campos();
        m_empresas m_empresas = new m_empresas();
        m_empresas.cbx_empresas(cbx_j_empresa);

        c_almacen.validar_almacen();
        txt_j_id.setText(c_almacen.getId() + "");
        txt_j_nombre.setText(c_almacen.getNombre());
        txt_j_direccion.setText(c_almacen.getDireccion());
        txt_j_ciudad.setText(c_almacen.getCiudad());
        txt_j_ticketera.setText(c_almacen.getTicketera());
        txt_j_ubigeo.setText(c_almacen.getUbigeo());
        txt_j_telefono.setText(c_almacen.getTelefono());
        c_empresa.setId(c_almacen.getEmpresa());
        c_empresa.validar_empresa();
        cbx_j_empresa.getModel().setSelectedItem(new cla_empresa(c_empresa.getId(), c_empresa.getRuc(), c_empresa.getRazon()));

        jd_reg_tienda.setVisible(true);
    }//GEN-LAST:event_btn_modificarActionPerformed

    private void llamar_ver_documentos() {
        jd_ver_doc_sunat.setModal(true);
        jd_ver_doc_sunat.setSize(400, 350);
        jd_ver_doc_sunat.setLocationRelativeTo(null);
        String query = "select da.id_tido, ds.descripcion, da.serie, da.numero "
                + "from documentos_almacen as da "
                + "inner join documentos_sunat as ds on ds.id_tido= da.id_tido "
                + "where da.id_almacen = '" + c_almacen.getId() + "' "
                + "order by ds.descripcion asc";
        c_mis_documentos.mostrar(t_documentos, query);
        jd_ver_doc_sunat.setVisible(true);
    }

