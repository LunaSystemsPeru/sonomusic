    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        double cantidad = Double.parseDouble(jTable1.getValueAt(fila_seleccionada, 3).toString());
        double precio = Double.parseDouble(jTable1.getValueAt(fila_seleccionada, 4).toString());
        int stock = this.c_kardex.obtener_suma_kardex();

        c_kardex.obtener_codigo();
        c_kardex.setFecha(c_varios.getFechaActual());
        c_kardex.setId_tido(9);
        c_kardex.setId_tipo_movimiento(14);
        c_kardex.setSerie(c_varios.obtener_anio() + "");
        c_kardex.setNumero(c_kardex.getId_kardex());
        c_kardex.setId_usuario(id_usuario);
        //ajuste segun tienda
        //si diferencia es mayor
        if (cantidad > stock) {
            c_kardex.setCant_salida(0);
            c_kardex.setCant_ingreso(cantidad - stock);
            c_kardex.setCosto_salida(0);
            c_kardex.setCosto_ingreso(precio);
        }

        if (stock > cantidad) {
            c_kardex.setCant_salida(stock - cantidad);
            c_kardex.setCant_ingreso(0);
            c_kardex.setCosto_salida(precio);
            c_kardex.setCosto_ingreso(0);
        }

        if (c_kardex.regitrar()) {
            jd_ajuste_producto.dispose();
            c_mis_productos.mis_productos(query, jTable1);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

